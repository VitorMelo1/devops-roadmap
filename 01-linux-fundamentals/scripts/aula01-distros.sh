# ─── O QUE É UMA DISTRIBUIÇÃO? ───────────────────────────────────────────────
# Uma distribuição (distro) é uma versão do Linux criada por uma comunidade
# com um objetivo específico. Todas compartilham o mesmo kernel, mas diferem
# em filosofia, ferramentas e público-alvo.

echo "--- O que é uma Distribuição? ---"
echo ""
echo "Linux = Kernel (núcleo do sistema)"
echo "Distro = Kernel + ferramentas + filosofia + comunidade"
echo ""
echo "Todas usam o mesmo kernel — mas cada uma tem seu propósito."
echo ""

# ─── PRINCIPAIS DISTROS ───────────────────────────────────────────────────────

echo "--- Principais Distribuições ---"
echo ""
echo "  Debian      → software 100% livre (GPL), base de muitas outras"
echo "  Ubuntu      → baseada no Debian, foco em acessibilidade"
echo "  Slackware   → simplicidade, filosofia própria"
echo "  Red Hat     → ambientes empresariais, suporte pago"
echo "  Conectiva   → brasileira, anos 90-2000 (descontinuada)"
echo ""

echo "--- Os 4 Pilares que diferenciam as distros ---"
echo ""
echo "  1. Propósito  → necessidade específica (servidor, desktop, segurança)"
echo "  2. Filosofia  → software livre vs. proprietário"
echo "  3. Ferramentas → pacotes open/closed source permitidos"
echo "  4. Liberdade  → uso educacional, comercial ou pessoal"
echo ""

# ─── ONDE O LINUX ESTÁ ───────────────────────────────────────────────────────

echo "--- Onde o Linux é usado (você usa todo dia sem saber) ---"
echo ""
echo "  Cloud    → AWS, GCP e Azure rodam Linux"
echo "  Mobile   → Android é Linux modificado"
echo "  TV       → smart TVs e roteadores usam Linux embarcado"
echo "  Servidores → maioria dos servidores web e bancos de dados"
echo "  DevOps   → Docker, Kubernetes e CI/CD rodam em Linux"
